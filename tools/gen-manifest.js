const fs = require("node:fs");
const path = require("node:path");
const { execFileSync } = require("node:child_process");

const root = path.resolve(__dirname, "..");
const packagePath = path.join(root, "package.json");
const sourcePath = path.join(root, "p5.gysin.js");
const examplesDir = path.join(root, "examples");
const curatedPath = path.join(__dirname, "manifest.curated.json");
const outputPath = path.join(root, "docs", "p5.gysin.manifest.json");

function fail(message) {
  process.stderr.write(`gen-manifest: ${message}\n`);
  process.exit(1);
}

if (!fs.existsSync(packagePath)) fail("package.json not found");
if (!fs.existsSync(sourcePath)) fail("p5.gysin.js not found");
if (!fs.existsSync(curatedPath)) fail("tools/manifest.curated.json not found");

const pkg = JSON.parse(fs.readFileSync(packagePath, "utf8"));
const source = fs.readFileSync(sourcePath, "utf8");
const curated = JSON.parse(fs.readFileSync(curatedPath, "utf8"));
delete curated._comment;

const methodOrder = ["line", "rect", "circle", "polygon", "path", "text", "textCutup"];
const shapeMethods = methodOrder.filter(function (name) {
  return new RegExp(`\\n\\s{4}${name}\\(`).test(source);
});

const exportOrder = ["exportSVG", "exportJSON", "exportHPGL"];
const exportsList = exportOrder.filter(function (name) {
  return new RegExp(`\\n\\s{4}${name}\\(`).test(source);
}).map(function (name) {
  return name.replace(/^export/, "");
});

let examples = [];
if (fs.existsSync(examplesDir)) {
  examples = fs.readdirSync(examplesDir, { withFileTypes: true })
    .filter(function (entry) {
      if (!entry.isDirectory()) return false;
      const dir = path.join(examplesDir, entry.name);
      return fs.existsSync(path.join(dir, "index.html")) &&
        fs.existsSync(path.join(dir, "sketch.js"));
    })
    .map(function (entry) {
      return entry.name;
    })
    .sort();
}

let commit = "unknown";
const gitCandidates = [
  "git",
  process.env.GIT,
  process.env.USERPROFILE && path.join(
    process.env.USERPROFILE,
    ".cache",
    "codex-runtimes",
    "codex-primary-runtime",
    "dependencies",
    "native",
    "git",
    "cmd",
    "git.exe"
  )
].filter(Boolean);

for (const gitPath of gitCandidates) {
  try {
    commit = execFileSync(gitPath, ["rev-parse", "--short", "HEAD"], {
      cwd: root,
      stdio: ["ignore", "pipe", "ignore"]
    }).toString().trim();
    break;
  } catch (error) {
    commit = "unknown";
  }
}

const manifest = {
  $schema_note: "Auto-generated by tools/gen-manifest.js. Do NOT hand-edit. Mechanical fields derive from package.json, p5.gysin.js, and examples/; curated fields come from tools/manifest.curated.json.",
  version: pkg.version,
  commit: commit,
  generated: new Date().toISOString().slice(0, 10),
  pages_url: "https://seb-prjcts-be.github.io/p5.gysin/docs/p5.gysin.manifest.json",
  p5js_target: curated.p5js_target,
  audience: curated.audience,
  shape_methods: shapeMethods,
  exports: exportsList,
  examples: examples,
  removed_apis: curated.removed_apis,
  added_apis: curated.added_apis
};

const json = `${JSON.stringify(manifest, null, 2)}\n`;
let previous = null;
try {
  previous = fs.readFileSync(outputPath, "utf8");
} catch (error) {
  previous = null;
}

if (previous === json) {
  console.log(`gen-manifest: up to date (v${pkg.version}, ${examples.length} examples)`);
  process.exit(0);
}

fs.writeFileSync(outputPath, json, "utf8");
console.log(`gen-manifest: wrote ${path.relative(root, outputPath)} (v${pkg.version}, ${examples.length} examples)`);
